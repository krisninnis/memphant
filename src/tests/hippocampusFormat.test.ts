// ─── Secret sanitisation ──────────────────────────────────────────────────────

describe('generateHippocampusMarkdown - secret sanitisation', () => {
  it('redacts OpenAI keys in currentState', () => {
    const secret = makeOpenAiKey();
    const output = generateHippocampusMarkdown(makeProject({ currentState: `Key: ${secret}` }));
    expect(output).not.toContain(secret);
    expect(output).toContain('[REDACTED]');
  });

  it('redacts Anthropic keys in goals', () => {
    const secret = makeAnthropicKey();
    const output = generateHippocampusMarkdown(makeProject({ goals: [`Use ${secret}`] }));
    expect(output).not.toContain(secret);
    expect(output).toContain('[REDACTED]');
  });

  it('redacts GitHub tokens in next steps', () => {
    const secret = makeGitHubToken();
    const output = generateHippocampusMarkdown(
      makeProject({ nextSteps: [`Token: ${secret}`] }),
    );
    expect(output).not.toContain(secret);
    expect(output).toContain('[REDACTED]');
  });

  it('redacts secrets inside the project charter', () => {
    const secret = makeOpenAiKey();
    const output = generateHippocampusMarkdown(
      makeProject({ projectCharter: `Never expose ${secret}` }),
    );
    expect(output).not.toContain(secret);
    expect(output).toContain('[REDACTED]');
  });

  it('redacts secrets in open questions', () => {
    const secret = makeAnthropicKey();
    const output = generateHippocampusMarkdown(
      makeProject({ openQuestions: [`Should we use ${secret}?`] }),
    );
    expect(output).not.toContain(secret);
    expect(output).toContain('[REDACTED]');
  });

  it('redacts secrets in decision rationale', () => {
    const secret = makeGitHubToken();
    const output = generateHippocampusMarkdown(
      makeProject({
        decisions: [{ decision: 'Use CI', rationale: `Token: ${secret}` }],
      }),
    );
    expect(output).not.toContain(secret);
    expect(output).toContain('[REDACTED]');
  });

  it('redacts Stripe live keys', () => {
    const secret = makeStripeLiveKey();
    const output = generateHippocampusMarkdown(
      makeProject({ currentState: `Stripe: ${secret}` }),
    );
    expect(output).not.toContain(secret);
    expect(output).toContain('[REDACTED]');
  });

  it('always redacts database connection strings (strict patterns always active)', () => {
    const secret = 'postgres://user:pass@host:5432/mydb';
    const output = generateHippocampusMarkdown(
      makeProject({ currentState: `DB: ${secret}` }),
    );
    expect(output).not.toContain(secret);
    expect(output).toContain('[REDACTED]');
  });

  it('does not redact normal project content', () => {
    const output = generateHippocampusMarkdown(makeProject());
    expect(output).toContain('My SaaS App');
    expect(output).toContain('Launch MVP');
    expect(output).toContain('Use Tauri for desktop');
    expect(output).not.toContain('[REDACTED]');
  });
});

// ─── linkedFolder path exclusion ──────────────────────────────────────────────

describe('generateHippocampusMarkdown - linkedFolder.path exclusion', () => {
  it('never includes a POSIX linked folder path', () => {
    const project = makeProject({
      linkedFolder: {
        path: '/Users/kris/secret/project-path',
        scanHash: 'abc',
        lastScannedAt: '',
      },
    });
    const output = generateHippocampusMarkdown(project);
    expect(output).not.toContain('/Users/kris/secret/project-path');
  });

  it('never includes a Windows-style linked folder path', () => {
    const project = makeProject({
      linkedFolder: {
        path: 'C:\\Users\\thoma\\private\\memephant',
        scanHash: 'abc',
        lastScannedAt: '',
      },
    });
    const output = generateHippocampusMarkdown(project);
    expect(output).not.toContain('C:\\Users\\thoma\\private\\memephant');
    expect(output).not.toContain('C:/Users/thoma/private/memephant');
  });

  it('redacts the linked folder path embedded in the charter', () => {
    const linkedPath = 'C:\\Users\\kris\\private\\memephant';
    const project = makeProject({
      projectCharter: `Do not expose ${linkedPath}`,
      linkedFolder: { path: linkedPath, scanHash: 'abc', lastScannedAt: '' },
    });
    const output = generateHippocampusMarkdown(project);
    expect(output).not.toContain(linkedPath);
    expect(output).toContain('[REDACTED]');
  });

  it('redacts the linked folder path embedded in current state', () => {
    const linkedPath = '/Users/kris/projects/my-app';
    const project = makeProject({
      currentState: `Working in ${linkedPath}`,
      linkedFolder: { path: linkedPath, scanHash: 'abc', lastScannedAt: '' },
    });
    const output = generateHippocampusMarkdown(project);
    expect(output).not.toContain(linkedPath);
  });

  it('redacts forward-slash variant of a Windows path', () => {
    const winPath = 'C:\\Users\\thoma\\secret';
    const project = makeProject({
      currentState: `Path is C:/Users/thoma/secret`,
      linkedFolder: { path: winPath, scanHash: 'abc', lastScannedAt: '' },
    });
    const output = generateHippocampusMarkdown(project);
    expect(output).not.toContain('C:/Users/thoma/secret');
  });
});

// ─── Edge cases ───────────────────────────────────────────────────────────────

describe('generateHippocampusMarkdown - edge cases', () => {
  it('handles a project with all empty arrays gracefully', () => {
    const project = makeProject({
      goals: [],
      rules: [],
      decisions: [],
      nextSteps: [],
      openQuestions: [],
      importantAssets: [],
    });
    expect(() => generateHippocampusMarkdown(project)).not.toThrow();
    const output = generateHippocampusMarkdown(project);
    expect(output.length).toBeGreaterThan(0);
  });

  it('shows (none) when currentState is empty', () => {
    const output = generateHippocampusMarkdown(makeProject({ currentState: '' }));
    expect(output).toContain('(none)');
  });

  it('handles a very long charter without crashing', () => {
    const project = makeProject({ projectCharter: 'A'.repeat(5000) });
    expect(() => generateHippocampusMarkdown(project)).not.toThrow();
  });

  it('handles special characters in the project name', () => {
    const project = makeProject({ name: '<script>alert("xss")</script>' });
    const output = generateHippocampusMarkdown(project);
    expect(output).toBeDefined();
    expect(output.length).toBeGreaterThan(0);
  });

  it('handles a decision with no rationale', () => {
    const project = makeProject({
      decisions: [{ decision: 'Use TypeScript' }],
    });
    const output = generateHippocampusMarkdown(project);
    expect(output).toContain('Use TypeScript');
    expect(output).not.toContain('Rationale: undefined');
  });

  it('produces a string with consistent section count for same input', () => {
    const project = makeProject();
    const out1 = generateHippocampusMarkdown(project);
    const out2 = generateHippocampusMarkdown(project);
    // Strip the generated ISO timestamp before comparing line count.
    const stripTs = (s: string) =>
      s.replace(/generated: [0-9TZ:.-]+/, 'generated: [TS]');
    expect(stripTs(out1).split('\n').length).toBe(stripTs(out2).split('\n').length);
  });

  it('does not include linkedFolder scanHash or lastScannedAt', () => {
    const project = makeProject({
      linkedFolder: {
        path: '/tmp/proj',
        scanHash: 'deadbeef12345',
        lastScannedAt: '2026-01-01T00:00:00Z',
      },
    });
    const output = generateHippocampusMarkdown(project);
    expect(output).not.toContain('deadbeef12345');
    // lastScannedAt leaking is acceptable (it's a timestamp, not sensitive),
    // but the path must not appear.
    expect(output).not.toContain('/tmp/proj');
  });
});
