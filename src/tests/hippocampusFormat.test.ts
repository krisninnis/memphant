import {
  generateHippocampusMarkdown,
  generateDefaultCharter,
  HIPPOCAMPUS_SCHEMA_VERSION,
} from '../utils/hippocampusFormat';
import type { ProjectMemory } from '../types/memphant-types';

// ─── Helpers ──────────────────────────────────────────────────────────────────

function makeProject(overrides: Partial<ProjectMemory> = {}): ProjectMemory {
  return {
    schema_version: 1,
    id: 'test_hippocampus',
    name: 'My SaaS App',
    summary: 'A SaaS product for project context management.',
    goals: ['Launch MVP', 'Get first 10 customers'],
    rules: ['Ship fast', 'Talk to users weekly'],
    decisions: [
      { decision: 'Use Tauri for desktop', rationale: 'Small bundle, native performance' },
    ],
    currentState: 'Pre-launch. MVP is 80% done.',
    nextSteps: ['Set up Stripe', 'Write landing page copy'],
    openQuestions: ['What pricing model?'],
    importantAssets: ['src/main.ts', 'src/store.ts'],
    aiInstructions: 'Help me think like a product founder.',
    changelog: [
      {
        timestamp: new Date().toISOString(),
        field: 'general',
        action: 'added',
        summary: 'Project created',
        source: 'app',
      },
    ],
    checkpoints: [],
    platformState: {},
    ...overrides,
  };
}

// Build secret-like strings at runtime so GitHub push protection
// does not flag the repository contents themselves.
function joinParts(...parts: string[]): string {
  return parts.join('');
}

function makeOpenAiKey(): string {
  return joinParts('sk-', 'AbCdEfGhIjKlMnOpQrStUv1234567890');
}

function makeAnthropicKey(): string {
  return joinParts(
    'sk-ant-api03-',
    'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz012345',
  );
}

function makeGitHubToken(): string {
  return joinParts('ghp_', 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghij');
}

function makeStripeLiveKey(): string {
  return joinParts('sk', '_live_', '1234567890abcdefghijklmnop');
}

// ─── Structure ────────────────────────────────────────────────────────────────

describe('generateHippocampusMarkdown - structure', () => {
  it('returns a non-empty string', () => {
    const output = generateHippocampusMarkdown(makeProject());
    expect(typeof output).toBe('string');
    expect(output.length).toBeGreaterThan(0);
  });

  it('includes the protocol header comment', () => {
    const output = generateHippocampusMarkdown(makeProject());
    expect(output).toContain('hippocampus.md — Memory Core File Protocol v1');
    expect(output).toContain('Generated by Memephant. Do not edit manually.');
  });

  it('includes the schema version in the header', () => {
    const output = generateHippocampusMarkdown(makeProject());
    expect(output).toContain(`hippocampus/${HIPPOCAMPUS_SCHEMA_VERSION}`);
  });

  it('HIPPOCAMPUS_SCHEMA_VERSION is 1.0', () => {
    expect(HIPPOCAMPUS_SCHEMA_VERSION).toBe('1.0');
  });

  it('includes the project id in the header', () => {
    const output = generateHippocampusMarkdown(makeProject({ id: 'proj_xyz' }));
    expect(output).toContain('project-id: proj_xyz');
  });

  it('includes a generated timestamp in the header', () => {
    const before = new Date().toISOString().slice(0, 10);
    const output = generateHippocampusMarkdown(makeProject());
    expect(output).toContain(before);
  });

  it('includes the project name as the main heading', () => {
    const output = generateHippocampusMarkdown(makeProject());
    expect(output).toContain('# My SaaS App — Memory Core');
  });

  it('always includes the Charter section', () => {
    const output = generateHippocampusMarkdown(makeProject({ projectCharter: 'Ship safely.' }));
    expect(output).toContain('## Charter');
  });

  it('includes the Current State section', () => {
    const output = generateHippocampusMarkdown(makeProject());
    expect(output).toContain('## Current State');
    expect(output).toContain('Pre-launch. MVP is 80% done.');
  });

  it('includes Goals', () => {
    const output = generateHippocampusMarkdown(makeProject());
    expect(output).toContain('## Goals');
    expect(output).toContain('Launch MVP');
    expect(output).toContain('Get first 10 customers');
  });

  it('includes Rules', () => {
    const output = generateHippocampusMarkdown(makeProject());
    expect(output).toContain('## Rules');
    expect(output).toContain('Ship fast');
  });

  it('includes Decisions with rationale', () => {
    const output = generateHippocampusMarkdown(makeProject());
    expect(output).toContain('## Decisions');
    expect(output).toContain('Use Tauri for desktop');
    expect(output).toContain('Small bundle, native performance');
  });

  it('includes Next Steps', () => {
    const output = generateHippocampusMarkdown(makeProject());
    expect(output).toContain('## Next Steps');
    expect(output).toContain('Set up Stripe');
  });

  it('includes Open Questions', () => {
    const output = generateHippocampusMarkdown(makeProject());
    expect(output).toContain('## Open Questions');
    expect(output).toContain('What pricing model?');
  });

  it('includes Important Assets', () => {
    const output = generateHippocampusMarkdown(makeProject());
    expect(output).toContain('## Important Assets');
    expect(output).toContain('src/main.ts');
  });

  it('includes the footer with protocol name', () => {
    const output = generateHippocampusMarkdown(makeProject());
    expect(output).toContain('Memory Core File Protocol v1');
    expect(output).toContain('do not edit this file manually');
  });
});

// ─── Charter ──────────────────────────────────────────────────────────────────

describe('generateHippocampusMarkdown - charter', () => {
  it('includes charter content when present', () => {
    const output = generateHippocampusMarkdown(
      makeProject({ projectCharter: 'Always prefer minimal safe edits.' }),
    );
    expect(output).toContain('## Charter');
    expect(output).toContain('Always prefer minimal safe edits.');
  });

  it('generates default charter from project fields when charter is empty string', () => {
    const output = generateHippocampusMarkdown(makeProject({ projectCharter: '' }));
    expect(output).toContain('## Charter');
    expect(output).toContain('Auto-generated');
    expect(output).toContain('Launch MVP'); // goal from makeProject
  });

  it('generates default charter when charter is undefined', () => {
    const project = makeProject();
    delete (project as Partial<ProjectMemory>).projectCharter;
    const output = generateHippocampusMarkdown(project);
    expect(output).toContain('## Charter');
    expect(output).toContain('Auto-generated');
    expect(output).toContain('Pre-launch. MVP is 80% done.'); // currentState from makeProject
  });

  it('generates default charter when charter is whitespace only', () => {
    const output = generateHippocampusMarkdown(makeProject({ projectCharter: '   ' }));
    expect(output).not.toContain('(no charter set)');
    expect(output).toContain('Auto-generated');
  });

  it('user-authored charter takes precedence over auto-generated content', () => {
    const output = generateHippocampusMarkdown(
      makeProject({ projectCharter: 'Always prefer minimal safe edits.' }),
    );
    expect(output).toContain('Always prefer minimal safe edits.');
    expect(output).not.toContain('Auto-generated');
  });

  it('trims leading and trailing whitespace from the charter', () => {
    const output = generateHippocampusMarkdown(
      makeProject({ projectCharter: '\n  Prefer safe changes.  \n' }),
    );
    expect(output).toContain('Prefer safe changes.');
  });
});

// ─── generateDefaultCharter unit tests ───────────────────────────────────────

describe('generateDefaultCharter', () => {
  it('includes the auto-generated label', () => {
    const result = generateDefaultCharter(makeProject());
    expect(result).toContain('Auto-generated');
  });

  it('includes currentState in synthesised text', () => {
    const result = generateDefaultCharter(makeProject({ currentState: 'MVP is live.' }));
    expect(result).toContain('MVP is live.');
  });

  it('falls back to summary when currentState is empty', () => {
    const result = generateDefaultCharter(
      makeProject({ currentState: '', summary: 'A SaaS product.' }),
    );
    expect(result).toContain('A SaaS product.');
  });

  it('includes goals in synthesised text', () => {
    const result = generateDefaultCharter(makeProject({ goals: ['Reach 100 users', 'Ship v2'] }));
    expect(result).toContain('Reach 100 users');
    expect(result).toContain('Ship v2');
  });

  it('uses singular label for a single goal', () => {
    const result = generateDefaultCharter(makeProject({ goals: ['Ship MVP'] }));
    expect(result).toContain('Goal: Ship MVP.');
  });

  it('uses plural label for multiple goals', () => {
    const result = generateDefaultCharter(makeProject({ goals: ['Ship MVP', 'Get users'] }));
    expect(result).toContain('Goals:');
  });

  it('includes rules in synthesised text', () => {
    const result = generateDefaultCharter(makeProject({ rules: ['Ship fast', 'Talk to users'] }));
    expect(result).toContain('Ship fast');
    expect(result).toContain('Talk to users');
  });

  it('includes key decisions (text only, no rationale)', () => {
    const result = generateDefaultCharter(
      makeProject({
        decisions: [{ decision: 'Use Tauri', rationale: 'Fast binaries' }],
      }),
    );
    expect(result).toContain('Use Tauri');
    // Rationale belongs in the Decisions section, not the synthesised charter.
    expect(result).not.toContain('Fast binaries');
  });

  it('returns empty string when project has no content at all', () => {
    const result = generateDefaultCharter(
      makeProject({
        currentState: '',
        summary: '',
        goals: [],
        rules: [],
        decisions: [],
      }),
    );
    expect(result).toBe('');
  });
});

