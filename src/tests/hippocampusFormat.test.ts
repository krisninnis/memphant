// ─── Important Assets hardening ───────────────────────────────────────────────

describe('generateHippocampusMarkdown - important asset hardening', () => {
  it('normalizes backslashes to forward slashes', () => {
    const output = generateHippocampusMarkdown(
      makeProject({
        importantAssets: ['public-site\\index.html', 'server\\server.js'],
      }),
    );

    expect(output).toContain('public-site/index.html');
    expect(output).toContain('server/server.js');
    expect(output).not.toContain('public-site\\index.html');
  });

  it('caps important assets to 20 useful entries', () => {
    const assets = Array.from({ length: 30 }, (_, index) => `src/file-${index + 1}.ts`);
    const output = generateHippocampusMarkdown(makeProject({ importantAssets: assets }));
    const bullets = getSectionBullets(output, 'Important Assets');

    expect(bullets).toHaveLength(20);
  });

  it('omits suspicious asset filenames', () => {
    const output = generateHippocampusMarkdown(
      makeProject({
        importantAssets: [
          'public-site/index.html',
          'pwordsec.txt',
          '.env',
          'server/private-key.pem',
          'server/token-cache.json',
        ],
      }),
    );

    expect(output).toContain('public-site/index.html');
    expect(output).not.toContain('pwordsec.txt');
    expect(output).not.toContain('.env');
    expect(output).not.toContain('private-key.pem');
    expect(output).not.toContain('token-cache.json');
  });

  it('omits mojibake or broken filenames', () => {
    const output = generateHippocampusMarkdown(
      makeProject({
        importantAssets: [
          'public-site/index.html',
          'docs/2┤®┼ÈÔ· BRIGHTFOUNDRY ÈÃ÷ MONTHLY CHECK-I.txt',
          'docs/¡â¯╔ WEBSITE & APP DEVELOPMENT BUSINESS.txt',
        ],
      }),
    );

    expect(output).toContain('public-site/index.html');
    expect(output).not.toContain('2┤®┼ÈÔ·');
    expect(output).not.toContain('¡â¯╔');
  });

  it('omits generic noisy scratch files', () => {
    const output = generateHippocampusMarkdown(
      makeProject({
        importantAssets: [
          'public-site/index.html',
          'TASK NAME.txt',
          'TASK NAME7.txt',
          'docs/Untitled.txt',
        ],
      }),
    );

    expect(output).toContain('public-site/index.html');
    expect(output).not.toContain('TASK NAME.txt');
    expect(output).not.toContain('TASK NAME7.txt');
    expect(output).not.toContain('Untitled.txt');
  });

  it('deduplicates important assets case-insensitively', () => {
    const output = generateHippocampusMarkdown(
      makeProject({
        importantAssets: ['public-site/index.html', 'PUBLIC-SITE/INDEX.HTML'],
      }),
    );
    const bullets = getSectionBullets(output, 'Important Assets');

    expect(bullets).toHaveLength(1);
  });
});

