describe('generateHippocampusMarkdown - charter', () => {
  it('includes charter content when present', () => {
    const output = generateHippocampusMarkdown(
      makeProject({ projectCharter: 'Always prefer minimal safe edits.' }),
    );

    expect(output).toContain('## Charter');
    expect(output).toContain('Always prefer minimal safe edits.');
  });

  it('generates default charter from project fields when charter is empty string', () => {
    const output = generateHippocampusMarkdown(makeProject({ projectCharter: '' }));

    expect(output).toContain('## Charter');
    expect(output).toContain('Auto-generated');
    expect(output).toContain('Launch MVP');
  });

  it('generates default charter when charter is undefined', () => {
    const project = makeProject();
    delete (project as Partial<ProjectMemory>).projectCharter;

    const output = generateHippocampusMarkdown(project);

    expect(output).toContain('## Charter');
    expect(output).toContain('Auto-generated');
    expect(output).toContain('Pre-launch. MVP is 80% done.');
  });

  it('generates default charter when charter is whitespace only', () => {
    const output = generateHippocampusMarkdown(makeProject({ projectCharter: '   ' }));

    expect(output).not.toContain('(no charter set)');
    expect(output).toContain('Auto-generated');
  });

  it('user-authored charter takes precedence over auto-generated content', () => {
    const output = generateHippocampusMarkdown(
      makeProject({ projectCharter: 'Always prefer minimal safe edits.' }),
    );

    expect(output).toContain('Always prefer minimal safe edits.');
    expect(output).not.toContain('Auto-generated');
  });

  it('trims leading and trailing whitespace from the charter', () => {
    const output = generateHippocampusMarkdown(
      makeProject({ projectCharter: '\n  Prefer safe changes.  \n' }),
    );

    expect(output).toContain('Prefer safe changes.');
  });
});

